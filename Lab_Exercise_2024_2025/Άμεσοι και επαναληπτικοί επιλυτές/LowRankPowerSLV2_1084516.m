function x = LowRankPowerSLV2_1084516(A, u, v, k, b)
% LowRankPowerSLV2_id
% Υπολογίζει ρητά τον πίνακα B^k, όπου B = A+u* v'', και στη συνέχεια λύνει
% το σύστημα B^k x  = b με άμεση μέθοδο.
%
% Προσοχή: Αν και ο B είναι αραιός (μετά rank–1 ενημέρωση) το B^k μπορεί
% να μην είναι πια αραιός, οπότε αυτή η μέθοδος μπορεί να μην είναι κατάλληλη
% για πολύ μεγάλα n και k.


B = A + u*v';

Bk = B;

for i = 2:k

    Bk = Bk * B;

end

x = Bk \ b;

end
