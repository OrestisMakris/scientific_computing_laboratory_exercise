
% Δοκιμή με έλεγχο ακρίβειας
n = 128; % δύναμη του 2
A = [rand(n,1), 2+rand(n,1), rand(n,1)]; % τυχαίο τριδιαγώνιο σύστημα
A(1,1) = 0; 
A(end,3) = 0; 
B = rand(n, 5);

X_my = tridReduct_my4AM(A, B);

% Επίλυση με τον ενσωματωμένο solver
T = spdiags([A(:,1), A(:,2), A(:,3)], [-1, 0, 1], n, n);
X_ref = T \ B;

% Υπολογισμός της μέγιστης νόρμας διαφοράς
error_norm = max(max(abs(X_my - X_ref)));

% Έλεγχος ακρίβειας
fprintf('Μέγιστη νόρμα διαφοράς: %.2e\n', error_norm);

% Υπολογισμός πολυπλοκότητας O(log n)
fprintf('Ο χρόνος εκτέλεσης είναι O(log n) λόγω Κυκλικής Αναγωγής.\n');
